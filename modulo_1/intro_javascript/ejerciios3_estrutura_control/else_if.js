var dia =  parseInt( prompt("Dijite un un numero para saver el dia de la semana") );

if (dia === 1) {
    console.log("Lunes");
}
else if (dia === 2) {
    console.log("Martes");
}
else if (dia === 3) {
    console.log("Miercoles");
}
else if (dia === 4) {
    console.log("Jueves");
}
else if (dia === 5) {
    console.log("Viesnes");
}
else if (dia === 6) {
    console.log("Sabado");
}
else if (dia === 7) {
    console.log("Domingo");
}
 